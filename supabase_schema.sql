-- 1. TABLES
-- ============================================================

-- 1a. Profiles (extends auth.users)
create table if not exists profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  username text unique not null,
  avatar_url text,
  level int default 1,
  xp int default 0,
  streak int default 0,
  created_at timestamptz default now()
);

-- 1b. Workout sessions (solo + duel mode)
create table if not exists workout_sessions (
  id uuid primary key default gen_random_uuid(),
  user_id uuid references profiles(id) on delete cascade not null,
  exercise_type text not null check (exercise_type in (
    'push_up', 'squat', 'crunch', 'pull_up', 'plank', 'lunge', 'shoulder_press'
  )),
  rep_count int not null,
  duration_seconds int,
  created_at timestamptz default now()
);

-- 1c. Duel rooms
create table if not exists duel_rooms (
  id uuid primary key default gen_random_uuid(),
  status text default 'waiting' check (status in ('waiting', 'active', 'finished')),
  exercise_type text not null check (exercise_type in (
    'push_up', 'squat', 'crunch', 'pull_up', 'plank', 'lunge', 'shoulder_press'
  )),
  duration_seconds int default 60,
  start_time timestamptz,
  winner_id uuid references profiles(id),
  created_at timestamptz default now()
);

-- 1d. Duel players (real-time rep tracking)
create table if not exists duel_players (
  id uuid primary key default gen_random_uuid(),
  room_id uuid references duel_rooms(id) on delete cascade not null,
  user_id uuid references profiles(id) on delete cascade not null,
  reps int default 0,
  ready boolean default false,
  joined_at timestamptz default now(),
  unique(room_id, user_id)
);

-- 1e. Friendships (arkadas sistemi: istek gonderme/kabul/red)
create table if not exists friendships (
  id uuid primary key default gen_random_uuid(),
  user_id uuid references profiles(id) on delete cascade not null,
  friend_id uuid references profiles(id) on delete cascade not null,
  status text default 'pending' check (status in ('pending', 'accepted', 'blocked')),
  created_at timestamptz default now(),
  unique(user_id, friend_id)
);

-- 1f. Challenges (haftalik challenge sistemi)
create table if not exists weekly_challenges (
  id text primary key,
  type text not null check (type in ('rep_goal', 'time_trial', 'streak_days')),
  title text not null,
  description text,
  exercise_type text check (exercise_type in (
    'push_up', 'squat', 'crunch', 'pull_up', 'plank', 'lunge', 'shoulder_press'
  )),
  target_value int not null,
  xp_reward int default 500,
  start_date timestamptz not null,
  end_date timestamptz not null
);

-- 1g. Challenge progress (her kullanicinin challenge ilerlemesi)
create table if not exists challenge_progress (
  id uuid primary key default gen_random_uuid(),
  challenge_id text references weekly_challenges(id) on delete cascade not null,
  user_id uuid references profiles(id) on delete cascade not null,
  progress int default 0,
  completed boolean default false,
  completed_at timestamptz,
  unique(challenge_id, user_id)
);

-- 1h. User achievements (kilitsiz basarimlar)
create table if not exists user_achievements (
  id bigint generated by default as identity primary key,
  user_id uuid references profiles(id) on delete cascade not null,
  achievement_id text not null check (achievement_id in (
    'first_rep', 'club_100', 'club_1000', 'streak_7', 'streak_30',
    'first_win', 'no_pain_no_gain', 'early_bird', 'night_owl',
    'comeback_king', 'perfect_form', 'speed_demon', 'duelist', 'veteran'
  )),
  unlocked_at timestamptz default now(),
  unique (user_id, achievement_id)
);

-- 1i. Streak history (gunluk antrenman kaydi)
create table if not exists streak_history (
  id bigint generated by default as identity primary key,
  user_id uuid references profiles(id) on delete cascade not null,
  workout_date date not null,
  rep_count int default 0,
  xp_earned int default 0,
  unique (user_id, workout_date)
);

-- 1j. Friend requests log (arkadaslik istek gecmisi)
create table if not exists friend_requests (
  id bigint generated by default as identity primary key,
  from_user_id uuid references profiles(id) on delete cascade not null,
  to_user_id uuid references profiles(id) on delete cascade not null,
  status text default 'pending' check (status in ('pending', 'accepted', 'rejected')),
  created_at timestamptz default now(),
  unique (from_user_id, to_user_id)
);

