-- 5a. Leaderboard: top users by total reps
create or replace function get_leaderboard(limit_count int default 20)
returns table (
  user_id uuid,
  username text,
  avatar_url text,
  total_reps bigint,
  total_sessions bigint,
  level int,
  streak int
)
language sql
security definer
as $$
  select
    p.id,
    p.username,
    p.avatar_url,
    coalesce(sum(ws.rep_count), 0)::bigint as total_reps,
    count(ws.id)::bigint as total_sessions,
    p.level,
    p.streak
  from profiles p
  left join workout_sessions ws on ws.user_id = p.id
  group by p.id, p.username, p.avatar_url, p.level, p.streak
  order by total_reps desc
  limit limit_count;
$$;

-- 5b. Haftalik arkadas liderlik tablosu
create or replace function get_friend_leaderboard(user_id uuid, limit_count int default 20)
returns table (
  friend_id uuid,
  username text,
  total_reps bigint,
  level int,
  streak int
)
language sql
security definer
as $$
  select
    case when f.user_id = get_friend_leaderboard.user_id then f.friend_id else f.user_id end,
    p.username,
    coalesce(sum(ws.rep_count), 0)::bigint,
    p.level,
    p.streak
  from friendships f
  join profiles p on p.id = case when f.user_id = get_friend_leaderboard.user_id then f.friend_id else f.user_id end
  left join workout_sessions ws on ws.user_id = p.id
  where (f.user_id = get_friend_leaderboard.user_id or f.friend_id = get_friend_leaderboard.user_id)
    and f.status = 'accepted'
  group by p.id, p.username, p.level, p.streak
  order by total_reps desc
  limit limit_count;
$$;

-- 5c. Challenge siralamasi (challenge'da en cok rep yapanlar)
create or replace function get_challenge_leaderboard(challenge_id text, limit_count int default 20)
returns table (
  user_id uuid,
  username text,
  progress int,
  completed boolean
)
language sql
security definer
as $$
  select
    cp.user_id,
    p.username,
    cp.progress,
    cp.completed
  from challenge_progress cp
  join profiles p on p.id = cp.user_id
  where cp.challenge_id = get_challenge_leaderboard.challenge_id
  order by cp.progress desc
  limit limit_count;
$$;

